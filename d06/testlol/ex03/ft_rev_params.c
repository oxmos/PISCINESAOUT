int		ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int i;

	i = 0;
	while (--argc > i)
	{
		ft_putstr(argv[argc]);
		ft_putchar('\n');
	}
	return (0);
}
