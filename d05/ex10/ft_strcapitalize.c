char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[0] >= 'a' && str[0] <= 'z')
			str[0] -= 32;
		if (str[i] < 48 || (str[i] > 57 && str[i] < 65) ||
				(str[i] > 90 && str[i] < 97) || str[i] > 122)
			if (str[i + 1] >= 'a' && str[i + 1] <= 'z')
				str[i + 1] -= 32;
		return (str);
	}
}
